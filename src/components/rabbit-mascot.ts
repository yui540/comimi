export function renderRabbitMascot(option?: MascotOption): HTMLElement | null {
  if (option === false) {
    return null;
  }

  const wrap = document.createElement("div");
  wrap.className = "comimi-rabbit";

  if (option && "render" in option) {
    wrap.append(option.render());
    return wrap;
  }

  if (option && "src" in option) {
    const img = document.createElement("img");
    img.className = "comimi-rabbit-img";
    img.src = option.src;
    img.alt = option.alt ?? "";
    img.draggable = false;
    wrap.append(img);
    return wrap;
  }

  wrap.innerHTML = DEFAULT_RABBIT_SVG;
  return wrap;
}
